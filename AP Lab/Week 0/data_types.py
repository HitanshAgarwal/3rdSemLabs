# Different number data types
# Integer
a = 20
print(type(a))

# Float
b = 21.23
print(type(b))

# Boolean
c = True
d = False
print(type(c))
print(type(d))

# String
e = "Hello World"
print(type(e))

# Complex
f = 3 + 4j
print(type(f))

# List
list1 = ["apple","banana", "watermelon"]
print(type(list1))

# Tuple
tup = ("apple","banana", "watermelon")
print(type(tup))

# Dictionary
dict1 = {"name":"SK", "age":19}
print(type(dict1))
